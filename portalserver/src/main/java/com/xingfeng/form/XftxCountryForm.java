package com.xingfeng.form;

@SuppressWarnings("serial")
public class XftxCountryForm implements java.io.Serializable{

	private String countryId;
	private String countryName;
	private String countryNameEn;
	private String countryTime;
	
	public String getCountryId() {
		return countryId;
	}
	public void setCountryId(String countryId) {
		this.countryId = countryId;
	}
	public String getCountryName() {
		return countryName;
	}
	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}
	public String getCountryNameEn() {
		return countryNameEn;
	}
	public void setCountryNameEn(String countryNameEn) {
		this.countryNameEn = countryNameEn;
	}
	public String getCountryTime() {
		return countryTime;
	}
	public void setCountryTime(String countryTime) {
		this.countryTime = countryTime;
	}
	
}
